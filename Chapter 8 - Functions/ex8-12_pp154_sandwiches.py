def make_sandwich(*toppings):
    print("Making sandwich")
    print("")
    for topping in toppings:
        print("Adding " + topping)
    print(".")
    print("..")
    print("...")
    print("Finished!")

make_sandwich("Bacon", "Lettuce", "Tomato")
make_sandwich("Bacon", "Egg")
make_sandwich("Chicken", "Aioli")
